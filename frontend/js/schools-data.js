// 搜索学校函数
function searchSchools(query, limit = 10) {
    if (!query || query.length < 1) return [];
    
    const results = [];
    const queryLower = query.toLowerCase();
    
    // 搜索所有类型的学校
    Object.values(SCHOOLS_DATA).forEach(schools => {
        schools.forEach(school => {
            if (school.toLowerCase().includes(queryLower) && results.length < limit) {
                results.push(school);
            }
        });
    });
    
    return results;
}

